Misión, visión y valores

<br>

-> VirtAcad tiene como objetivo ser un academia virtual reconocida de formación online entre los estudiantes. 
 <br>   
-> Pretendemos que en VirtAcad todos los alumnos, la comunidad que lo conforma, sean los auténticos protagonistas de su formación, y que sirva para crear profesionales 
<br>
reconocidos en las empresas y que favorezca su desarrollo profesional y personal. 
<br>
Podríamos destacar como nuestros valores:
<br>
-> Una excelente atención al alumno.
<br>
-> El empleo del mejor material de estudio y aprendizaje.
<br>
-> La experiencia profesional y docente de nuestro profesorado.
<br>
-> Nuestros acuerdos de colaboración con empresas de diversos sectores.
<br>
-> Garantía de calidad universitaria de los estudios.
<br>
-> La ilusión y compromiso del equipo.
<br>
-> El uso de una metodología abierta, apoyada en las nuevas tecnologías.
